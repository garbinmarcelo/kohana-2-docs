<h1><a name="translations" id="translations">Translations</a></h1>
<div class="level1">

<p>

Translations of the user guide aren&#039;t currently set up, but they will work as follows:
</p>

<p>
The root namespace is in english.
Other languages will copy the namespace structure of the root but will reside in their own namespace using their language code (and localization if needed).
</p>

<p>
For example, the French translation of <code>general/configuration</code> would be in <code>fr/general/configuration</code>.
</p>

<p>
On each page there will be a list of all available languages, as well as an indication of which languages the current page has been translated to.
</p>

</div>

<h1><a name="translations_of_kohana" id="translations_of_kohana">Translations of Kohana</a></h1>
<div class="level1">

<p>
Translations of Kohana start off by translating the English i18n files in the system/i18n directory to the language of choice. For more information see the <a href="http://forum.kohanaphp.com/index.php/topic,610.msg3771.php#new" class="urlextern" title="http://forum.kohanaphp.com/index.php/topic,610.msg3771.php#new"  rel="nofollow">forum</a>.

</p>

