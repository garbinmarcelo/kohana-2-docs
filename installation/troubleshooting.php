<h1><a name="troubleshooting" id="troubleshooting">Troubleshooting</a></h1>
<div class="level1">

<p>

Programming is art? So is debugging! A few tips to keep your code bug free and how Kohana can help.

</p>
<ul>
<li class="level1"><div class="li"> See what you&#039;re doing. Kohana makes this one easy:</div>
<ul>
<li class="level2"><div class="li"> Throw variables at <code><a href="../core/kohana.php#debug" class="wikilink1" title="core:kohana">Kohana::debug()</a></code>.</div>
</li>
<li class="level2"><div class="li"> Turn on the <a href="../libraries/profiler.php" class="wikilink1" title="libraries:profiler">Profiler</a> to see global variables and the executed queries.</div>
</li>
</ul>
</li>
<li class="level1"><div class="li"> Eliminate possible causes of the bug. Isolate the problem.</div>
</li>
<li class="level1"><div class="li"> Check the Kohana <a href="../core/kohana.php#methods_log" class="wikilink1" title="core:kohana">log</a> files.</div>
</li>
<li class="level1"><div class="li"> If your using MySQL <a href="http://www.howtogeek.com/howto/database/monitor-all-sql-queries-in-mysql/" class="urlextern" title="http://www.howtogeek.com/howto/database/monitor-all-sql-queries-in-mysql/"  rel="nofollow">turning on query logging</a> will help you understand what queries Kohana is running on your database.</div>
</li>
</ul>

<p>

Can&#039;t figure things out on your own?

</p>
<ul>
<li class="level1"><div class="li"> Post your problem in the <a href="http://forum.kohanaphp.com/" class="urlextern" title="http://forum.kohanaphp.com/"  rel="nofollow">Kohana Forum</a> (after having <a href="http://forum.kohanaphp.com/index.php?action=search" class="urlextern" title="http://forum.kohanaphp.com/index.php?action=search"  rel="nofollow">searched</a> first, of course).</div>
</li>
<li class="level1"><div class="li"> Drop in at #kohana on irc.freenode.net</div>
</li>
</ul>

