<h1><a name="features" id="features">Features</a></h1>
<div class="level1">
<ul>
<li class="level1"><div class="li"> Highly secure</div>
</li>
<li class="level1"><div class="li"> Extremely lightweight</div>
</li>
<li class="level1"><div class="li"> Short learning curve</div>
</li>
<li class="level1"><div class="li"> Uses the MVC pattern</div>
</li>
<li class="level1"><div class="li"> 100% UTF-8 compatible</div>
</li>
<li class="level1"><div class="li"> Loosely coupled architecture</div>
</li>
<li class="level1"><div class="li"> Extremely easy to extend</div>
</li>
</ul>

</div>

<h1><a name="technology" id="technology">Technology</a></h1>
<div class="level1">
<ul>
<li class="level1"><div class="li"> Strict PHP5 OOP</div>
</li>
<li class="level1"><div class="li"> Simple database abstraction using <acronym title="Structured Query Language">SQL</acronym> helpers</div>
</li>
<li class="level1"><div class="li"> Multiple session drivers (native, database, and cookie)</div>
</li>
<li class="level1"><div class="li"> Powerful event handler allows small modifications dynamically</div>
</li>
<li class="level1"><div class="li"> Originally based on CodeIgniter</div>
</li>
</ul>

