<h2><a name="kohana_2.0" id="kohana_2.0">Kohana 2.0</a></h2>
<div class="level2">

<p>
November 2007
</p>

<p>
Kohana version 2.0 is released. PHP5 based.
New fully OOP framework, no legacy code, includes modules, built on the cascading resources concept.
</p>

